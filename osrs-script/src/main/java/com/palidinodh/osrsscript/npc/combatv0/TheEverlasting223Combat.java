import lombok.var;

class TheEverlasting223Combat extends NpcCombat {
  @Override
  public List<NpcCombatDefinition> getCombatDefinitions() {
    var combat = NpcCombatDefinition.builder();
    combat.id(NpcId.THE_EVERLASTING_223);
    combat.hitpoints(NpcCombatHitpoints.total(230));
    combat.stats(NpcCombatStats.builder().attackLevel(187).defenceLevel(120).build());
    combat.aggression(NpcCombatAggression.PLAYERS);
    combat.deathAnimation(6347).blockAnimation(6346);

    var style = NpcCombatStyle.builder();
    style.type(NpcCombatStyleType.MELEE_CRUSH);
    style.damage(NpcCombatDamage.maximum(24));
    style.animation(6345).attackSpeed(6);
    style.projectile(NpcCombatProjectile.id(335));
    combat.style(style.build());


    return Arrays.asList(combat.build());
  }
}
